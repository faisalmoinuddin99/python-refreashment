# Escape Seqence in python

"""
Python escape sequence ignore

To ignore all the escape sequences in the string, we have to make a 
string as a raw string using ‘r’ before the string. 
After that escape sequence will also be consider as normal characters. 
Let's take an example.
"""
print(r"Hello\nWorld")

# output: Hello\nWorld
